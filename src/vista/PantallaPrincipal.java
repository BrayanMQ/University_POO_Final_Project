    private void btn_ModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ModificarActionPerformed
        
        String id = JOptionPane.showInputDialog(this, "Indique la c√©dula del estudiante que desea modificar.", "Modificar estudiante", JOptionPane.QUESTION_MESSAGE);
        
        if (id != null ) {//Si no le da al bot√≥n cancelar
             Estudiante estudiante = Controlador.getSingletonInstance().getGestorEstudiantes().buscarEstudiante(id);
             
             if (estudiante != null) {
                
                 new ModificarEstudiante(this, rootPaneCheckingEnabled, estudiante).setVisible(true);
                 
             } else {
             
                 JOptionPane.showMessageDialog(this, "No se ha encontrado el estudiante con c√©dula " + id + ".", "Modificar estudiante", JOptionPane.INFORMATION_MESSAGE);
                 
             }
        }     
    }//GEN-LAST:event_btn_ModificarActionPerformed

